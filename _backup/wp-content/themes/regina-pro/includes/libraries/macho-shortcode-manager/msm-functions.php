function get_sliders() {
	$args         = array(
		'post_type'      => 'slider',
		'posts_per_page' => -1,
		'post_Status'    => 'published',
	);
	$slider_posts = get_posts( $args );
	$sliders      = array(
		'' => 'Select Slider',
	);

	foreach ( $slider_posts as $slider ) {
		$sliders[ $slider->ID ] = $slider->post_title;
	}

	return $sliders;

}
